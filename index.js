// 猫の名簿
const cats = ["ごまちゃん", "パンくん", "レオくん", "ヒロシくん", "西森くん", "大林くん", "市川くん", "和田ちゃん", "さんまさん", "武くん"];

// i というカウンターを作って、0から順番に1ずつ増やしていく
/*
🔍 for文の3つのルール（カッコの中身）
① let i = 0; （スタート地点）
意味: 「今、0番目の名簿を見ています」という変数 i（インデックスの略）を作ります。

なぜ 0 から？: プログラミングの世界では、配列（名簿）の1番目は 0 と数える決まりがあるからです。

② i < cats.length; （いつまで続けるか）
意味: 「i が名簿の数（length）より小さい間は、ずっと続けてね！」という条件です。

例: 猫が3匹（length が 3）なら、i が 0, 1, 2 の間は実行されます。3になった瞬間に「あ、もう名簿は終わりだ」と判断して止まります。

③ i++ （次に進む準備）
意味: 先ほど学んだインクリメントですね！「1回処理が終わったら、i を 1 増やして次の番号に進めてね」という命令です。

🏃‍♂️ 1周ごとの動きを実況中継
const cats = ["ごまちゃん", "パンくん", "レオくん"]; だとした場合：

【1周目】

i は 0 です。

cats[0]（ごまちゃん）を捕まえて、「(0+1)番目の猫はごまちゃん」と表示。

終わったら i++ で i が 1 になります。

【2周目】

i は 1 です。

cats[1]（パンくん）を捕まえて、「(1+1)番目の猫はパンくん」と表示。

終わったら i++ で i が 2 になります。

【3周目】

i は 2 です。

cats[2]（レオくん）を捕まえて、「(2+1)番目の猫はレオくん」と表示。

終わったら i++ で i が 3 になります。

【終了】

i が 3 になりました。

i < 3 という条件を満たさなくなったので、マシンが停止します。

💡 なぜ (i + 1) と書いているの？
プログラム上は 0番目、1番目…と数えますが、キヨヒコさんのブログを読む読者（人間）からすると「0番目の猫」と言われると違和感がありますよね。

そこで、**「表示するときだけ、0に1を足して人間向けの番号（1番、2番…）に変換する」**という優しい配慮をしているわけです。
*/
const container = document.getElementById("container"); // 親玉を捕まえる
for (let i = 0; i < cats.length; i++) {
    console.log( (i + 1) + "番目の猫は" + cats[i] );
    // 【手作業版】idを見てそこだけ書き込みに行くコード
    document.getElementById("cat" + (i +1)).innerText = cats[i];

    //【divタグから自動書き込み版】
    // 1. 新しい div タグをメモリの中に作成する
    const newDiv = document.createElement("div");
    // 2. 作った div に ID をつける（例: cat1, cat2...）
    newDiv.id = "cat" + (i + 1);
    // 3. 中身のテキストを入れる
    newDiv.innerText = (i + 1) + "番目の猫は " + cats[i];
    // 4. 【重要】親玉の中に、今作った div を「追加（配膳）」する
    container.appendChild(newDiv);
}